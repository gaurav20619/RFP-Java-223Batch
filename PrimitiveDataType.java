public class PrimitiveDataType {
    static int val1;
    static float val2;
    static double val3;
    static long val4;
    static String val5;
    static boolean val6;
   public static void main(String[] args) {
      System.out.println("Default values of all the Data types are given below");
      System.out.println("int = " + val1);
      System.out.println("float = " + val2);
      System.out.println("double = " + val3);
      System.out.println("long = " + val4);
      System.out.println("String = " + val5);
      System.out.println("boolean = " + val6);
   }
}

